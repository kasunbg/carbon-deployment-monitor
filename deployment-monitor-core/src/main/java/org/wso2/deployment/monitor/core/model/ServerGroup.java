package org.wso2.deployment.monitor.core.model;

import java.util.ArrayList;
import java.util.List;

/**
 * The server group bean that correspond to the
 * serverGroup configuration in deployment-monitor.yaml.
 *
 */
public class ServerGroup {
    private String name = "wso2carbon";
    private List<String> hosts = new ArrayList<>();
    private boolean allowConcurrentExecution = false;
    private String trustStore = "";
    private String trustStorePassword = "";

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public List<String> getHosts() {
        return hosts;
    }

    public void setHosts(List<String> hosts) {
        this.hosts = hosts;
    }

    public boolean allowConcurrentExecution() {
        return allowConcurrentExecution;
    }

    public String getTrustStore() {
        return trustStore;
    }

    public void setTrustStore(String trustStore) {
        this.trustStore = trustStore;
    }

    public String getTrustStorePassword() {
        return trustStorePassword;
    }

    public void setTrustStorePassword(String trustStorePassword) {
        this.trustStorePassword = trustStorePassword;
    }
}
